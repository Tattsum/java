public class PrintStrIntVar {
    public static void main(String args[]) {
        int x = 12, y = 34;
        int ans = x + y;

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("ans = " + ans);
        System.out.println("x + y = " + x + y);
        System.out.println("(x + y) = " + (x + y));
    }
}
